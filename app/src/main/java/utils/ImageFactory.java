/**
 * 知识
 对同一个图片文件，分别将inPreferredConfig设置为ALPHA_8，RGB_565和ARGB_8888三个选项，按照之前的描述，一定可以得出bm1，bm2和bm3三个对象占用的内存大小一定是bm1 < bm2 < bm3这样的顺序。
 然而实际情况却并不是这样，这里如果将bm1，bm2和bm3中每个像素都取出来进行比较，在大多数情况下会发现，bm1，bm2和bm3中的内容完全相同。
 查看官方文档，可以看到对inPreferredConfig的描述是这样的。
 翻译过来是这样的：如果inPreferredConfig不为null，解码器会尝试使用此参数指定的颜色模式来对图片进行解码，
 如果inPreferredConfig为null或者在解码时无法满足此参数指定的颜色模式，解码器会自动根据原始图片的特征以及当前设备的屏幕位深，
 选取合适的颜色模式来解码，例如，如果图片中包含透明度，那么对该图片解码时使用的配置就需要支持透明度，默认会使用ARGB_8888来解码。
 也就是说inPreferredConfig指定的配置并非是一个强制选项，而是建议的（preferred）选项，Android在实际解码时会参考此参数的配置，
 但如果此配置不满足，Android会重新选取一个合适的配置来对图片进行解码。
 */


